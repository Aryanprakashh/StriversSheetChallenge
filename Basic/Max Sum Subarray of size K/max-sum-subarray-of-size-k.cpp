class Solution{   
public:
    long maximumSumSubarray(int k, vector<int> &arr , int n){
        // code here 
        long mx=INT_MIN;
        long sum=0;
        int i=0;
        for(int j=0;j<n;j++){
            sum+=arr[j];
            if(j-i+1<k)
                continue;
            
            else if(j-i+1==k){
                mx=max(mx,sum);
                sum-=arr[i];
                i++;
            }
        }
        return mx;
    }
};

